# Operadore entre numeros (int e float)

a = 2.5
b = 10

print(a + b) # soma
print(a - b) # subtracao
print(a * b) # multipliacao
print(a / b) # divisao
print(a % b) # modulo (resto da divisao)
print(a ** b) # expoenciacao

# Operacoes entre booleanos

x = True
y = False

print(x and y)
print(x or y)
print(not x)

